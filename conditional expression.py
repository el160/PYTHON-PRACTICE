#print or assign one or two values based o a condition
#syntax: value1 if condition else value2
#for instance x if condtion else y

num = 10
print("Positive" if num > 0 else "Negative") #Positive


num = 6
print("Even" if num % 2 == 0 else "Odd") #Even


age = 20
print("Eligible" if age >= 18 else "Not Eligible") #Eligible

a=10
b=20
print("a is greater" if a > b else "b is greater") #b is greater
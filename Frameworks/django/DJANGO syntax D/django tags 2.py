# in django tags are represented using {% %}
# these templates tags are a waying of telling django that there comes something else other than a basic HTML
#the tags allow doing of server programming b4 sending html to the client

# below is a list of template tags:

# 1. autoescape	- Specifies if autoescape mode is on or off
# 2. block	- Specifies a block section
# 3. comment	- Specifies a comment section
# 4. csrf_token	- Protects forms from Cross Site Request Forgeries
# 5. cycle	- Specifies content to use in each cycle of a loop
# 6. debug	- Specifies debugging information
# 7. extends	- Specifies a parent template
# 8. filter	- Filters content before returning it
# 9. firstof	- Returns the first not empty variable
# 10. for	- Specifies a for loop
# 11. if	- Specifies a if statement
# 12. ifchanged	- Used in for loops. Outputs a block only if a value has changed since the last iteration
# 13. include	- Specifies included content/template
# 14. load	- Loads template tags from another library
# 15. lorem	- Outputs random text
# 16. now	- Outputs the current date/time
# 17. regroup	- Sorts an object by a group
# 18. resetcycle	- Used in cycles. Resets the cycle
# 19. spaceless	- Removes whitespace between HTML tags
# 20. templatetag	- Outputs a specified template tag
# 21. url	- Returns the absolute URL part of a URL
# 22. verbatim	- Specifies contents that should not be rendered by the template engine
# 24. widthratio	- Calculates a width value based on the ratio between a given value and a max value
# 25. with	- Specifies a variable to use in the block
